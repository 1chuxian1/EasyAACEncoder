			memcpy(pbPCMBuffer + nCount, pbPCMTmpBuffer, nPCMRead);
			if(AAC_DEBUG) printf("%s:[%d] G711A -> PCM nStatus = 0\n",  __FUNCTION__, __LINE__);
			nCount += nPCMRead;
